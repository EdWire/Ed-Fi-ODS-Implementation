;WITH lea AS 
(
	SELECT l.LocalEducationAgencyId, ld.LocalEducationAgencyCategoryDescriptorId
	FROM #lea l
	JOIN edfi.Descriptor d
		ON l.LocalEducationAgencyCategoryCodeValue = d.CodeValue
		AND l.LocalEducationAgencyCategoryNamespace = d.Namespace
	JOIN edfi.LocalEducationAgencyCategoryDescriptor ld
		ON d.DescriptorId = ld.LocalEducationAgencyCategoryDescriptorId
	JOIN edfi.EducationOrganization eo
		ON l.LocalEducationAgencyId = eo.EducationOrganizationId
)
MERGE INTO edfi.LocalEducationAgency AS tgt
USING lea AS src 
	ON tgt.LocalEducationAgencyId = src.LocalEducationAgencyId
WHEN NOT MATCHED THEN 
	INSERT (LocalEducationAgencyId, LocalEducationAgencyCategoryDescriptorId)
	VALUES (src.LocalEducationAgencyId, src.LocalEducationAgencyCategoryDescriptorId)
WHEN MATCHED AND src.LocalEducationAgencyCategoryDescriptorId <> tgt.LocalEducationAgencyCategoryDescriptorId
	THEN UPDATE SET tgt.LocalEducationAgencyCategoryDescriptorId = src.LocalEducationAgencyCategoryDescriptorId
;

